void ssd1306_draw_char(ssd1306_t *dev, int x, int y, char c, uint8_t color) {
    // Verificar si el carácter está dentro del rango ASCII imprimible (32 a 126)
    if (c < 32 || c > 126) return;
    
    // Obtener el índice en el arreglo de la fuente
    uint8_t font_index = c - 32;

    for (uint8_t i = 0; i < 5; i++) { // La fuente tiene 5 columnas de ancho
        uint8_t line = font5x8[font_index][i];
        for (uint8_t j = 0; j < 8; j++) { // Y 8 filas de alto
            if (line & 0x01) {
                ssd1306_draw_pixel(dev, x + i, y + j, color);
            } else {
                ssd1306_draw_pixel(dev, x + i, y + j, !color); // Dibuja el fondo
            }
            line >>= 1;
        }
    }
}

void ssd1306_draw_string(ssd1306_t *dev, int x, int y, const char *str, uint8_t color) {
    int current_x = x;
    while (*str) {
        // Salto de línea manual
        if (*str == '\n') {
            y += 8; // Bajar 8 píxeles (altura de la fuente)
            current_x = x;
        } else {
            ssd1306_draw_char(dev, current_x, y, *str, color);
            current_x += 6; // Avanzar 5 píxeles de la letra + 1 píxel de espacio
        }
        str++;
    }
}

