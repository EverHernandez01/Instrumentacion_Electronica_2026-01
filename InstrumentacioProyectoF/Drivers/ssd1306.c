esp_err_t ssd1306_update(ssd1306_t *dev) {
    ssd1306_send_cmd(dev, 0x21);
    ssd1306_send_cmd(dev, 0);
    ssd1306_send_cmd(dev, SSD1306_WIDTH - 1);
    
    ssd1306_send_cmd(dev, 0x22);
    ssd1306_send_cmd(dev, 0);
    ssd1306_send_cmd(dev, (SSD1306_HEIGHT / 8) - 1);

    i2c_cmd_handle_t cmd_handle = i2c_cmd_link_create();
    i2c_master_start(cmd_handle);
    i2c_master_write_byte(cmd_handle, (dev->i2c_addr << 1) | I2C_MASTER_WRITE, true);
    i2c_master_write_byte(cmd_handle, OLED_CONTROL_BYTE_DATA_STREAM, true);
    i2c_master_write(cmd_handle, dev->buffer, sizeof(dev->buffer), true);
    i2c_master_stop(cmd_handle);
    
    esp_err_t ret = i2c_master_cmd_begin(dev->i2c_port, cmd_handle, 1000 / portTICK_PERIOD_MS);
    i2c_cmd_link_delete(cmd_handle);
    return ret;
}